let bitwiseOr = 5 | 3;
console.log("Bitwise Or: " + bitwiseOr); // Output: Bitwise Or: 7
let bitwiseXor = 5 ^ 3;
console.log("Bitwise Xor: " + bitwiseXor); // Output: Bitwise Xor: 6
